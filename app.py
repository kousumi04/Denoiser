@app.get("/health")
async def health() -> JSONResponse:
    return JSONResponse(
        {
            "status": "ok",
            "device": DEVICE,
            "checkpoint": str(Path(os.getenv("CHECKPOINT_PATH", str(DEFAULT_CKPT))).resolve()),
            "model_loaded": service is not None,
        }
    )


@app.post("/denoise")
async def denoise(
    request: Request,
    image: UploadFile = File(...),
    sigma: str | None = Form(default=None),
) -> Response:
    if service is None:
        raise HTTPException(status_code=503, detail="Model is still loading. Please retry shortly.")

    image_bytes = await image.read()
    if not image_bytes:
        raise HTTPException(status_code=400, detail="Uploaded file is empty.")

    if len(image_bytes) > MAX_UPLOAD_MB * 1024 * 1024:
        raise HTTPException(status_code=413, detail=f"Image exceeds {MAX_UPLOAD_MB} MB upload limit.")

    sigma_value = sigma if sigma is not None else request.query_params.get("sigma")
    parsed_sigma: float | None = None
    if sigma_value not in (None, ""):
        try:
            parsed_sigma = float(sigma_value)
        except ValueError as exc:
            raise HTTPException(status_code=400, detail="Sigma must be a number in [0, 1].") from exc
        if not 0.0 <= parsed_sigma <= 1.0:
            raise HTTPException(status_code=400, detail="Sigma must be a number in [0, 1].")

    try:
        output_bytes, used_sigma = service.denoise(image_bytes=image_bytes, sigma=parsed_sigma)
    except ValueError as exc:
        raise HTTPException(status_code=400, detail=str(exc)) from exc
    except Exception as exc:
        raise HTTPException(status_code=500, detail=f"Denoising failed: {exc}") from exc

    headers = {
        "x-denoise-sigma": f"{used_sigma:.6f}",
        "Content-Disposition": 'inline; filename="denoised-output.png"',
        "Cache-Control": "no-store",
    }
    return Response(content=output_bytes, media_type="image/png", headers=headers)
