DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
DEFAULT_CKPT = PROJECT_ROOT / "checkpoints" / "DRUNET_DENOISER.pt"
MAX_UPLOAD_MB = int(os.getenv("MAX_UPLOAD_MB", "12"))


class DenoiserService:
    def __init__(self, checkpoint_path: Path, device: str):
        self.checkpoint_path = checkpoint_path
        self.device = device
        self.model = self._load_model()

    def _load_model(self) -> DRUNet:
        if not self.checkpoint_path.is_file():
            raise FileNotFoundError(f"Checkpoint not found: {self.checkpoint_path}")

        model = DRUNet().to(self.device)
        try:
            state = torch.load(self.checkpoint_path, map_location=self.device, weights_only=True)
        except TypeError:
            state = torch.load(self.checkpoint_path, map_location=self.device)

        if isinstance(state, dict) and "model_state_dict" in state:
            state = state["model_state_dict"]
        if isinstance(state, dict) and "state_dict" in state:
            state = state["state_dict"]

        cleaned = {}
        for key, value in state.items():
            cleaned[key[7:] if key.startswith("module.") else key] = value

        model.load_state_dict(cleaned, strict=True)
        model.eval()
        return model

    @staticmethod
    def estimate_sigma(img_rgb: np.ndarray) -> float:
        gray = 0.299 * img_rgb[:, :, 0] + 0.587 * img_rgb[:, :, 1] + 0.114 * img_rgb[:, :, 2]
        med = np.median(gray)
        mad = np.median(np.abs(gray - med))
        sigma = 1.4826 * mad
        return float(np.clip(sigma, 0.01, 0.25))

    @staticmethod
    def decode_image(image_bytes: bytes) -> np.ndarray:
        try:
            pil_image = Image.open(io.BytesIO(image_bytes)).convert("RGB")
        except UnidentifiedImageError as exc:
            raise ValueError("Uploaded file is not a valid image.") from exc
        return np.asarray(pil_image, dtype=np.float32) / 255.0

    @staticmethod
    def encode_png(img_rgb: np.ndarray) -> bytes:
        clipped = np.clip(img_rgb, 0.0, 1.0)
        image = Image.fromarray((clipped * 255.0).round().astype(np.uint8), mode="RGB")
        buffer = io.BytesIO()
        image.save(buffer, format="PNG")
        return buffer.getvalue()

    def denoise(self, image_bytes: bytes, sigma: float | None) -> tuple[bytes, float]:
        img = self.decode_image(image_bytes)
        sigma_value = float(np.clip(sigma, 0.0, 1.0)) if sigma is not None else self.estimate_sigma(img)

        tensor = torch.from_numpy(img).permute(2, 0, 1).unsqueeze(0).to(self.device)
        sigma_tensor = torch.tensor([[sigma_value]], dtype=tensor.dtype, device=tensor.device)

        with torch.inference_mode():
            output = self.model(tensor, sigma=sigma_tensor)
            output = torch.clamp(output, 0.0, 1.0)

        output_np = output.squeeze(0).permute(1, 2, 0).cpu().numpy()
        return self.encode_png(output_np), sigma_value


service: DenoiserService | None = None


@asynccontextmanager
async def lifespan(_: FastAPI):
    global service

    checkpoint_path = Path(os.getenv("CHECKPOINT_PATH", str(DEFAULT_CKPT))).resolve()
    service = DenoiserService(checkpoint_path=checkpoint_path, device=DEVICE)
    try:
        yield
    finally:
        service = None


